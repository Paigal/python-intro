# Arrays
# definition: array is an ordered list
y = ["First thing", "Second thing"]
for x in y:
    print(x)

# Dictionaries
# definition: array is an ordered list
dictionary = {"Bonjour": "French", "Adios": "Spanish"}

print(dictionary["Bonjour"])

for key, value in dictionary.items():
    print(f"{key} : {value}")